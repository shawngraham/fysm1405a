class MimeTypes
{
	/**
	 * png image mime type
	 * Extension: png
	 * @var string
	 */
	const PNG = "image/png";
	
	/**
	 * jpeg image mime type
	 * Extension: jpg, jpeg
	 * @var string
	 */
	const JPG = "image/jpeg";
	
	/**
	 * png file mime type
	 * Extension: gif
	 * @var string
	 */
	const GIF = "image/gif";
	
	/**
	 * mp3 audio mime type
	 * Extension: mp3
	 * @var string
	 */
	const MP3 = "audio/mp3";
	
	/**
	 * mp4 audio mime type
	 * Extension: mp4
	 * @var string
	 */
	const MP4_audio = "audio/mp4";
	
	/**
	 * wav audio mime type
	 * Extension: wav
	 * @var string
	 */
	const WAV = "audio/wav";
	/**
	 * !! NOT Standard !!
	 * md2 model mime type
	 * Extension: md2
	 * @var string
	 */
	const MD2 = "model/md2";
	
	/**
	 * !! NOT Standard !!
	 * obj model mime type
	 * Extension: obj
	 * @var string
	 */
	const OBJ = "model/obj";
	
	/**
	 * 
	 * mp4 video mime type
	 * Extension: mp4
	 * @var string
	 */
	const MP4_video = "video/mp4";
	
	/**
	 * 
	 * quicktime video mime type
	 * Extension: mov, qt, mqv
	 * @var string
	 */
	const MOV = "video/quicktime";
	
	/**
	 * 
	 * m4v video mime type
	 * Extension: m4v
	 * @var string
	 */
	const M4V = "video/x-m4v";	
	
	/**
	 * 
	 * m4v video mime type
	 * Extension: m4v
	 * @var string
	 */
	const TEXT = "text/plain";	
}